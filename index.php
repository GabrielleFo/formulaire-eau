                <h4 >LES REGLES APPLICABLES</h4>

                <p>Le taux réduit de 10 % de la TVA est applicable aux prestations de services qui contribuent au bon fonctionnement des réseaux de distribution ou d'évacuation d'eau appartenant aux communes.</p><p> Cela inclut les prestations permettant d'assurer la gestion normale du service public municipal de l'eau, tant pour l'alimentation en eau potable que pour l'évacuation des eaux usées.</p>

                <ol>Pour déterminer si vos prestations répondent aux critères pour le taux réduit de 10 %, nous nous basons sur trois critères :
                <li>L'objet des prestations réalisées : Les prestations doivent être effectuées pour permettre aux communes d'assurer la gestion du service public municipal de l'eau.</li>
                <li>Les biens concernés : Seules les prestations portant sur l'eau ou sur le réseau de fourniture et d'évacuation de l'eau sont concernées.</li>
                <li>La nature des prestations fournies : Sont concernées par le taux intermédiaire les prestations portant sur les installations, à l'exclusion des travaux immobiliers, et celles effectuées sur l'eau.</li>
                </ol>
